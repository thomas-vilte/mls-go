func TestGenerateInteropTestVectors(t *testing.T) {
	// Generate test vectors
	tvs, err := GenerateInteropTestVectors()
	if err != nil {
		t.Fatalf("Failed to generate test vectors: %v", err)
	}

	t.Logf("Generated %d test vectors", len(tvs.Vectors))

	// Validate each vector
	for i, tv := range tvs.Vectors {
		t.Run(tv.Name, func(t *testing.T) {
			err := ValidateTestVector(&tv)
			if err != nil {
				t.Errorf("Test vector %d validation failed: %v", i, err)
			}
		})
	}
}

func TestExportImportTestVectors(t *testing.T) {
	// Generate test vectors
	tvs, err := GenerateInteropTestVectors()
	if err != nil {
		t.Fatalf("Failed to generate test vectors: %v", err)
	}

	// Create temporary directory
	tmpDir := t.TempDir()
	filename := filepath.Join(tmpDir, "test_vectors.json")

	// Export
	err = tvs.ExportToFile(filename)
	if err != nil {
		t.Fatalf("Failed to export test vectors: %v", err)
	}

	// Verify file exists
	_, err = os.Stat(filename)
	if err != nil {
		t.Fatalf("Exported file does not exist: %v", err)
	}

	// Import
	importedTVS, err := ImportFromFile(filename)
	if err != nil {
		t.Fatalf("Failed to import test vectors: %v", err)
	}

	// Verify imported data
	if importedTVS.Name != tvs.Name {
		t.Errorf("Name mismatch: expected %s, got %s", tvs.Name, importedTVS.Name)
	}

	if len(importedTVS.Vectors) != len(tvs.Vectors) {
		t.Errorf("Vector count mismatch: expected %d, got %d",
			len(tvs.Vectors), len(importedTVS.Vectors))
	}
}

func TestOneToOneJoinScenario(t *testing.T) {
	// This test validates the basic 1:1 join scenario
	// Scenario:
	// 1. Alice creates group
	// 2. Alice adds Bob
	// 3. Bob joins via Welcome

	gen := NewTestVectorGenerator(2) // MLS128DHKEMP256
	tv, err := gen.GenerateOneToOneJoin()
	if err != nil {
		t.Fatalf("Failed to generate test vector: %v", err)
	}

	t.Logf("Generated test vector:")
	t.Logf("  Cipher Suite: %d", tv.CipherSuite)
	t.Logf("  Group ID: %s...", tv.GroupID[:16])
	t.Logf("  Final Epoch: %d", tv.ExpectedEpoch)
	t.Logf("  Members: %d", tv.ExpectedNumMembers)

	// Basic validations
	if tv.ExpectedEpoch != 1 {
		t.Errorf("Expected epoch 1 after join, got %d", tv.ExpectedEpoch)
	}

	if tv.ExpectedNumMembers != 2 {
		t.Errorf("Expected 2 members after join, got %d", tv.ExpectedNumMembers)
	}

	// Note: Commits may be empty in simplified implementations
	// In full implementation, this would contain the actual Commit message

	if len(tv.Welcomes) == 0 {
		t.Error("Expected at least one welcome message")
	}
}

// TestCrossImplementationRoundTrip validates that Go can process
// test vectors generated by OpenMLS (when available).
// This test is skipped unless OPENMLS_TEST_VECTORS environment variable is set.
func TestCrossImplementationRoundTrip(t *testing.T) {
	// Skip unless explicitly enabled
	vectorFile := os.Getenv("OPENMLS_TEST_VECTORS")
	if vectorFile == "" {
		t.Skip("Skipping cross-implementation test. Set OPENMLS_TEST_VECTORS env var to enable.")
	}

	tvs, err := ImportFromFile(vectorFile)
	if err != nil {
		t.Fatalf("Failed to import OpenMLS test vectors: %v", err)
	}

	t.Logf("Imported %d test vectors from OpenMLS", len(tvs.Vectors))

	for i, tv := range tvs.Vectors {
		t.Run(tv.Name, func(t *testing.T) {
			err := ValidateTestVector(&tv)
			if err != nil {
				t.Errorf("Test vector %d (%s) validation failed: %v", i, tv.Name, err)
			}
		})
	}
}

