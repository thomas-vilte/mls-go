	// This is a documentation test explaining how to run interoperability tests
	t.Log(`
OpenMLS Interoperability Testing
================================

To test against OpenMLS:

1. Generate test vectors in Go:
   
   tvs, err := GenerateInteropTestVectors()
   err = tvs.ExportToFile("go_test_vectors.json")

2. Import in OpenMLS using their test vector runner:
   
   cargo run --bin test_vector_runner -- --input go_test_vectors.json

3. Export test vectors from OpenMLS:
   
   cargo run --bin test_vector_generator -- --output openmls_vectors.json

4. Validate in Go:
   
   export OPENMLS_TEST_VECTORS=openmls_vectors.json
   go test ./interop/... -v -run TestCrossImplementationRoundTrip

Test Vector Format
==================

The test vectors follow the MLS Interop format defined at:
https://github.com/mlswg/mls-implementations/blob/master/test-vectors/

Each vector contains:
- CipherSuite configuration
- Group creation parameters
- Key packages
- Commits and Welcome messages
- Expected state after operations

Current Limitations
===================

- Only MLS128DHKEMP256 cipher suite is tested
- Basic credential type only
- Single-commit scenarios only
- External commits not yet tested

To implement full interoperability:
1. Complete Welcome message generation
2. Implement full UpdatePath generation
3. Add external commit support
4. Support all cipher suites defined in RFC 9420
`)
